import java.util.*;
class swapping
{
public static void main(String[] args)
{
int a,b,c;
Scanner ob=new Scanner(System.in);
a=ob.nextInt();
b=ob.nextInt();
System.out.println("a value ="+a);
System.out.println("b value ="+b);
c=a;
a=b;
b=c;
System.out.println("swapping a value="+a);
System.out.println("swapping b value=" +b);
}
}
